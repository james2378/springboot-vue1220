<script>
  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            jiaofeibianhao : false,
            jiaofeileixing : false,
            jiaofeijine : false,
            jiaofeimingxi : false,
            jiaofeishijian : false,
            xuehao : false,
            xueshengxingming : false,
            ispay : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          jiaofeibianhao: this.getUUID(),
          jiaofeileixing: '',
          jiaofeijine: '',
          jiaofeimingxi: '',
          jiaofeishijian: '',
          xuehao: '',
          xueshengxingming: '',
          ispay: '',
        },
        jiaofeileixingOptions: [],
        xuehaoOptions: [],
        rules: {
          jiaofeibianhao: [
          ],
          jiaofeileixing: [
          ],
          jiaofeijine: [
            { required: true, message: '缴费金额不能为空', trigger: 'blur' },
            { validator: this.$validate.isNumber, trigger: 'blur' },
          ],
          jiaofeimingxi: [
          ],
          jiaofeishijian: [
          ],
          xuehao: [
            { required: true, message: '学号不能为空', trigger: 'blur' },
          ],
          xueshengxingming: [
          ],
          ispay: [
          ],
        },
      };
    },
    computed: {



    },
    created() {
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.jiaofeishijian = this.getCurDateTime()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='jiaofeibianhao'){
              this.ruleForm.jiaofeibianhao = obj[o];
              this.ro.jiaofeibianhao = true;
              continue;
            }
            if(o=='jiaofeileixing'){
              this.ruleForm.jiaofeileixing = obj[o];
              this.ro.jiaofeileixing = true;
              continue;
            }
            if(o=='jiaofeijine'){
              this.ruleForm.jiaofeijine = obj[o];
              this.ro.jiaofeijine = true;
              continue;
            }
            if(o=='jiaofeimingxi'){
              this.ruleForm.jiaofeimingxi = obj[o];
              this.ro.jiaofeimingxi = true;
              continue;
            }
            if(o=='jiaofeishijian'){
              this.ruleForm.jiaofeishijian = obj[o];
              this.ro.jiaofeishijian = true;
              continue;
            }
            if(o=='xuehao'){
              this.ruleForm.xuehao = obj[o];
              this.ro.xuehao = true;
              continue;
            }
            if(o=='xueshengxingming'){
              this.ruleForm.xueshengxingming = obj[o];
              this.ro.xueshengxingming = true;
              continue;
            }
          }
        }
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
          }
        });
        this.jiaofeileixingOptions = "学费,住宿费,书本费,水费,电费".split(',')
        this.$http.get('option/xuesheng/xuehao', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.xuehaoOptions = res.data.data;
          }
        });
      },
      // 下二随
      xuehaoChange () {
        this.$http.get('follow/xuesheng/xuehao?columnValue=' + this.ruleForm.xuehao, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            if(res.data.data.xueshengxingming){
              this.ruleForm.xueshengxingming = res.data.data.xueshengxingming
            }
          }
        });
      },

    // 多级联动参数
      // 多级联动参数
      info(id) {
        this.$http.get('xueshengjiaofei/detail/${id}', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

        //更新跨表属性
        var crossuserid;
        var crossrefid;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                            crossuserid=Number(localStorage.getItem('userid'));
                            crossrefid=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossrefid && crossuserid) {
                 this.ruleForm.crossuserid=crossuserid;
                 this.ruleForm.crossrefid=crossrefid;
                 var params = {
                     page: 1,
                     limit: 10,
                     crossuserid:crossuserid,
                     crossrefid:crossrefid,
                 }
                 this.$http.get('xueshengjiaofei/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post('xueshengjiaofei/add', this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post('xueshengjiaofei/add', this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
    }
  };
</script>

